/**
 * Generates `supabase/migrations/0003_seed_data.sql` from the in-repo seed
 * arrays. Run with:
 *
 *   pnpm tsx scripts/generate-seed-sql.ts
 *
 * The generated SQL inserts every brand, product, product colour, product
 * image, hero slide, and site_image with current default URLs. Re-run any
 * time the seed files change to regenerate the migration.
 */

import { writeFileSync } from "node:fs"
import { resolve } from "node:path"
import { BRANDS } from "../lib/seed-data/brands.seed"
import { PRODUCTS } from "../lib/seed-data/products.seed"
import { TEAM_MEMBERS } from "../lib/cms/team"

function slugify(value: string): string {
  return value
    .toLowerCase()
    .normalize("NFKD")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "")
}

function sqlString(value: string | null | undefined): string {
  if (value == null) return "NULL"
  return `'${value.replace(/'/g, "''")}'`
}

function sqlBool(value: boolean | undefined): string {
  return value ? "true" : "false"
}

function sqlTextArray(values: string[] | undefined): string {
  if (!values || values.length === 0) return `'{}'`
  const escaped = values.map((v) => `"${v.replace(/"/g, '\\"')}"`).join(",")
  return `'{${escaped}}'`
}

const lines: string[] = []
lines.push(
  "-- Auto-generated by scripts/generate-seed-sql.ts — do not edit by hand.",
  "-- Re-run that script after changing lib/seed-data/* or lib/cms/team.ts.",
  "--",
  "-- Apply AFTER 0001_init.sql and 0002_images_and_products.sql.",
  "-- Idempotent: every row uses ON CONFLICT DO UPDATE so running it more",
  "-- than once is safe and will refresh defaults without duplicating rows.",
  "",
  "begin;",
  "",
)

// ---------------------------------------------------------------------------
// brands
// ---------------------------------------------------------------------------
lines.push("-- brands -----------------------------------------------------------")
BRANDS.forEach((b, idx) => {
  lines.push(
    `insert into public.brands (slug, name, logo_url, website_url, description, categories, featured, is_active, sort_order)`,
    `values (${sqlString(b.slug)}, ${sqlString(b.name)}, ${sqlString(b.logoUrl || null)}, ${sqlString(b.website || null)}, ${sqlString(b.description)}, ${sqlTextArray(b.categories)}, ${sqlBool(b.featured)}, true, ${idx})`,
    `on conflict (slug) do update set`,
    `  name = excluded.name,`,
    `  logo_url = excluded.logo_url,`,
    `  website_url = excluded.website_url,`,
    `  description = excluded.description,`,
    `  categories = excluded.categories,`,
    `  featured = excluded.featured,`,
    `  sort_order = excluded.sort_order;`,
    "",
  )
})

// ---------------------------------------------------------------------------
// hero_slides — the four homepage marketing slides
// ---------------------------------------------------------------------------
lines.push("-- hero_slides ------------------------------------------------------")
const heroSlides = [
  { idx: 1, file: "/images/mainmemory/1.png" },
  { idx: 2, file: "/images/mainmemory/2.png" },
  { idx: 3, file: "/images/mainmemory/3.png" },
  { idx: 4, file: "/images/mainmemory/4.png" },
]
heroSlides.forEach((slide) => {
  const slug = `homepage-slide-${slide.idx}`
  lines.push(
    `insert into public.hero_slides (id, title, subtitle, cta_text, cta_url, image_url, bg_color, is_active, sort_order)`,
    `values (gen_random_uuid(), ${sqlString(`Homepage hero slide ${slide.idx}`)}, NULL, NULL, NULL, ${sqlString(slide.file)}, NULL, true, ${slide.idx})`,
    `on conflict do nothing;`,
    `-- key: ${slug}`,
    "",
  )
})

// ---------------------------------------------------------------------------
// site_images — keyed registry. Every <SiteImage imageId="..."> in the app
// resolves to one row here.
// ---------------------------------------------------------------------------
lines.push("-- site_images -----------------------------------------------------")

interface SiteImageRow {
  key: string
  label: string
  url: string
  alt: string
}

const siteImages: SiteImageRow[] = []

siteImages.push({
  key: "site.logo",
  label: "Site logo (header and footer)",
  url: "/images/mainmemory/promoshop-logo.png",
  alt: "PromoShop Studio",
})

siteImages.push({
  key: "about.hero",
  label: "About page hero image",
  url: "/images/mainmemory/11.png",
  alt: "Outside of the PromoShop building",
})

BRANDS.forEach((b) => {
  siteImages.push({
    key: `brand.${b.slug}.logo`,
    label: `Brand logo: ${b.name}`,
    url: b.logoUrl || "",
    alt: `${b.name} logo`,
  })
  siteImages.push({
    key: `brand.${b.slug}.lifestyle`,
    label: `Brand lifestyle backdrop: ${b.name}`,
    url: "",
    alt: `${b.name} lifestyle`,
  })
})

TEAM_MEMBERS.forEach((m) => {
  siteImages.push({
    key: `team.${slugify(m.name)}`,
    label: `Team photo: ${m.name} (${m.role})`,
    url: m.imagePath || "",
    alt: m.name,
  })
})

siteImages.forEach((img) => {
  lines.push(
    `insert into public.site_images (key, label, url, alt_text)`,
    `values (${sqlString(img.key)}, ${sqlString(img.label)}, ${sqlString(img.url)}, ${sqlString(img.alt)})`,
    `on conflict (key) do update set`,
    `  label = excluded.label,`,
    `  alt_text = excluded.alt_text;`,
    "",
  )
})

// ---------------------------------------------------------------------------
// products + product_colours + product_images
// ---------------------------------------------------------------------------
lines.push("-- products --------------------------------------------------------")
PRODUCTS.forEach((p, idx) => {
  const brandSlugs = (p.brands || []).map((name) => {
    const match = BRANDS.find(
      (b) => b.name === name || b.slug === slugify(name),
    )
    return match ? match.slug : slugify(name)
  })
  lines.push(
    `insert into public.products (sku, name, category, description, brand_slugs, genders, sizes, min_qty, deco_locations, deco_methods, is_active, sort_order)`,
    `values (${sqlString(p.sku)}, ${sqlString(p.name)}, ${sqlString(p.category)}, ${sqlString(p.description || null)}, ${sqlTextArray(brandSlugs)}, ${sqlTextArray(p.gender)}, ${sqlTextArray(p.sizes)}, ${p.minQty}, ${sqlTextArray(p.decoLocations)}, ${sqlTextArray(p.decoMethods)}, true, ${idx})`,
    `on conflict (sku) do update set`,
    `  name = excluded.name,`,
    `  category = excluded.category,`,
    `  description = excluded.description,`,
    `  brand_slugs = excluded.brand_slugs,`,
    `  genders = excluded.genders,`,
    `  sizes = excluded.sizes,`,
    `  min_qty = excluded.min_qty,`,
    `  deco_locations = excluded.deco_locations,`,
    `  deco_methods = excluded.deco_methods,`,
    `  sort_order = excluded.sort_order;`,
    "",
  )

  // Clear and reinsert colours + images. Doing this in a single
  // delete+insert pass keeps the script idempotent.
  lines.push(
    `delete from public.product_colours where product_sku = ${sqlString(p.sku)};`,
  )

  p.colours.forEach((c, colourIdx) => {
    const colourId = `gen_random_uuid()`
    const colourLabel = `colour_${slugify(c.name)}_${p.sku.replace(/\s+/g, "_")}`
    lines.push(
      `with new_colour as (`,
      `  insert into public.product_colours (product_sku, name, hex, sort_order)`,
      `  values (${sqlString(p.sku)}, ${sqlString(c.name)}, ${sqlString(c.hex)}, ${colourIdx})`,
      `  returning id`,
      `)`,
    )

    const images = c.images.filter((u) => !!u)
    if (images.length === 0) {
      lines.push(
        `select id from new_colour;  -- ${colourLabel}: no images in seed`,
        "",
      )
    } else {
      const valueRows = images
        .map(
          (url, imgIdx) =>
            `(${sqlString(p.sku)}, (select id from new_colour), ${sqlString(`${p.name} — ${c.name} — Image ${imgIdx + 1}`)}, ${sqlString(url)}, ${sqlString(`${p.name} in ${c.name}`)}, ${imgIdx})`,
        )
        .join(",\n  ")
      lines.push(
        `insert into public.product_images (product_sku, colour_id, label, url, alt_text, sort_order)`,
        `values`,
        `  ${valueRows};`,
        "",
      )
    }
  })
})

lines.push("commit;", "")

const outPath = resolve(__dirname, "..", "supabase", "migrations", "0003_seed_data.sql")
writeFileSync(outPath, lines.join("\n"))
console.log(`Wrote ${outPath} (${lines.length} lines)`)
